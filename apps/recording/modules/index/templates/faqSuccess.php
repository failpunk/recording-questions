        <h1 class="title">Frequently Asked Questions</h1>

        <p class="p4">We want to make this into the best recording community on the internet.  Here are a few things you may want to know as you set started.</p>

        <h3 class="subtitle">Q: What type of questions should you ask?</h3>

        <p class="p4">A: We want to be the home to any type of question about recording.  We know how many things can go wrong with all that great equipment and software you've managed to amass, and we all know how frustrating it can be.  No matter your skill level or studio size, any question is fair game.  Just be sure to make your questions detailed, clearly written, and related to recording. </p>

        <h3 class="subtitle">Q: What type of questions should we avoid?</h3>

        <p class="p4">A: Let's do our best to refrain from asking argumentative or combative questions -- leave that stuff for the forum boards.  This is a place to get together and exchange knowledge between experienced recording engineers and musicians.  <strong>Remember, your questions and answers should be as well thought out and executed as any of your own recordings</strong></p>



        <h3 class="section">Experience Points</h3>

        <h3 class="subtitle">Q: What are Experience Points?</h3>

        <p class="p4">A: Experience Points are the primary means by which Recording Questions rewards constructive site members.</p>


        <h3 class="subtitle">Q: How do I get Experience Points?</h3>

        <p class="p4">A: You can earn Experience Points by making positive contributions on the site such as:</p>
        
        <ul>
            <li>Generating up-votes from your peers on questions and/or answers that you post (+10pts)</li>
            <li>Posting an answer that is selected as being the ‘best’ by the person that posted the question (+15pts)</li>
            <li>Carefully reviewing the answers you receive after posting a question and designating one as the ‘best’ (+2pts)</li>
        </ul>


        <h3 class="subtitle">Q: Can Experience Points be taken away?  If so, what causes that to happen? </h3>

        <p class="p4">A: Yes, there a few reasons that Experience Points may be deducted.</p>

        <ul>
            <li>Getting down-voted by your peers on questions and/or answers that you post (-2pts)</li>
            <li>Casting down-votes on your peers’ questions and/or answers (-1pt)</li>
            <li>Posting offensive questions and/or answers (-100pts; see "Offensive" Content section for details)</li>
        </ul>

        
        <h3 class="subtitle">Q: Why do I get an Experience Point deducted for down-voting a question and/or answer posted by someone else?</h3>

        <p class="p4">A: The reason we deduct an Experience Point for casting a down-vote is because we want to ensure that down-votes remain a truer representation of inappropriate or unreliable content (rather than simply a gesture of disagreement or general dislike).  Being willing to give up a little bit to cast that vote demonstrates a deeper sense of integrity and will ultimately yield better information.</p>


        <h3 class="subtitle">Q: If Experience Points are taken away from me for placing down-votes, then can I expect Experience Points to be awarded to me when I place up-votes?</h3>

        <p class="p4">A: No, you are not awarded Experience Points for placing up-votes because we believe that would lead to artificial voting.  The sole purpose of up-voting is to reward constructive site members that contribute accurate, useful, relevant information; thus, are helping build a better recording community for all.</p>



        <h3 class="section">Offensive Content</h3>

        <h3 class="subtitle">Q: What types of questions and/or answers would be considered offensive and worthy of reporting via the offensive link?</h3>

        <p class="p4">A: Listed below are some key characteristics that would qualify questions and/or answers as offensive and would be worthy of reporting via the offensive link:</p>

         <ul>
            <li>Being entirely unrelated to audio recording</li>
            <li>Being discriminatory based on class, category, or group to which someone belongs rather than individual merit</li>
            <li>Using vulgar or profane language</li>
            <li>Making unwarranted personal attacks</li>
        </ul>
        

        <h3 class="subtitle">Q: Is there a penalty involved with reporting offensive questions and/or answers?</h3>

        <p class="p4">A: No, if you decide to report an offensive question and/or answer, you will not be penalized but we do keep an eye on suspisious activity and this includes excessive use of the offensive link.</p>


        <h3 class="subtitle">Q: Is there a penalty involved if somebody else reports my question and/or answer as offensive?</h3>

        <p class="p4">A: Yes, there is a penalty involved if somebody else reports your question and/or answer as offensive; however, in order to receive the penalty, there must be at least five community members reporting the offensive content in a particular post.</p>


        <h3 class="subtitle">Q: Is there a way for me to petition a penalty related to posting offensive questions and/or answers?</h3>

        <p class="p4">A: Sure, if you strongly believe that you have been erroneously penalized for posting offensive questions and/or answers, please send a message to admin@recordingquestions.com and include a brief explanation.  We will gladly review the data and do our best to work with you to get the situation resolved.</p>


        <h3 class="section">Permalink</h3>

        <h3 class="subtitle">Q: What does the permalink button do?</h3>

        <p class="p4">A: The permalink button provides you with a direct link to the particular answer.  For example, let's say you find an answer to a certain question that you think your friend would find useful.  By pressing the permalink button, the site gives you a direct link in the URL bar that you may send your friend links to the specific answer you found rather than having them navigate the site to find the information on their own.</p>


        <h3 class="section">Adding Gear To The Database</h3>

        <h3 class="subtitle">Q: Is there an easy way of finding the pictures of gear that I would like to add?</h3>

        <p class="p4">A: We use the following technique when adding images for new gear:</p>

         <ul>
            <li>Do a quick search on <strong>Google</strong> for the name of a piece of gear</li>
            <li>Click on the link in the upper-left of the page called <strong>"Images"<></li>
            <li>Click on a somewhat large image (we will resize it when you upload it) and then click the "See full size image"</li>
            <li>Finally, right-click and save the URL of the full-size image and use this in the upload image textbox.</li>
        </ul>

